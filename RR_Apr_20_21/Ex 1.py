# Creating a loop with range 1:100
for fizzbuzz in range(1,101):

# For a number divisible by 15, print 'FizzBuzz'
    if fizzbuzz % 15 == 0:
        print("FizzBuzz")

# For a number divisible by 3, print 'Fizz'
    elif fizzbuzz % 3 == 0:
        print("Fizz")

# For a number divisible by 5, print 'Buzz'
    elif fizzbuzz % 5 == 0:
        print("Buzz")

    else:
        print(fizzbuzz)





